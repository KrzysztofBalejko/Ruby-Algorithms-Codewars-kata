def get_planet_name(id)
    case id
    when id = 1
       "Mercury"
    when id = 2
       "Venus"
    when id = 3
       "Earth"
    when id = 4
       "Mars"
    when id = 5
       "Jupiter"
    when id = 6
       "Saturn"
    when id = 7
       "Uranus"
    when id = 8
       "Neptune"
   end
end
